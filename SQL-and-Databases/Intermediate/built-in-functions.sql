Column functions:
SUM(), MIN(), MAX(), AVG(), etc

Scalar Functions:
ROUND(), FLOOR(), ABS(), etc

String functions:
LENGTH(), UCASE(), LCASE(), DISTINCT(), etc.

Date-Time functions:
YEAR(), MONTH(), DAY(), DAYOFMONTH(), DAYOFWEEK(), DAYOFYEAR(), WEEK(), HOUR(), MINUTE(), SECOND(),

- DATE_ADD(DATE_COLUMN, INTERVAL 3 DAY) - to add 3 days to DATE_COLUMN value.
- FROM_DAYS(DATEDIFF(CURRENT_DATE, DATE_COLUMN)) - difference between the dates in yyyy-mm-dd